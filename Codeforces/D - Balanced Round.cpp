void solve() {
    /*
      Date: 29 - 06 - 2024
    */
    int n, x;
    cin >> n >> x;
    vii v(n);
    for(int i = 0; i < n; i++ ) cin >> v[i];
    sort(all(v));
    if(n == 1) {
        cout << 0 << nn;
        return;
    }
    vii diff;
    for(int i = 1; i < n; i++ ) {
        diff.push_back(abs(v[i] - v[i - 1]));
    } 
    int ans = 0;
    int cnt = 1;
    for(int i = 0; i < sz(diff); i++ ) {
        if(diff[i] <= x) cnt++;
        else cnt = 1;
        ans = max(ans, cnt);
    }
    cout << n - ans << nn;
    
} 
